var arr = [1, 5, 3, 6];
// This is a very simply array.
// You can check the length of an array by using the .length property
console.log(arr.length);  // will print 4, since there are 4 elements.
// Remember, the indexes of an array always start from 0.

// Let us print out a single element of an array, by using it's index
console.log(arr[2]);
// Will print out '3', since the indexes start from 0;

// Thus, printint out
console.log(arr[5]);
// will obviously print out undefined, since that index doesn't exist.
// Yes, JS is smart :)

